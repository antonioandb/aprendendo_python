# um tipo de dado pode ser visto como uma classificação que o python coloca em um valor literal, ou seja, um tipo de dado é uma categoria que define o tipo de valor que uma variável pode armazenar. e tambem ajuda o python a decidir quais operações podem ser realizadas com esses valores. por exemplo se uma variavel é do tipo inteiro o python sabe que pode realizar operações matemeticas como adição, subtração etc.

# tipos de dados 

#string:
"Antonio" # isso é um texto, o python ve ele como uma sequencia de caracteres, e classifica ele como uma string, ou seja, o tipo de dado é string, uma string usa aspas simples ou duplas para ser definida.

#inteiro:
10 # isso é um numero inteiro, o python classifica ele como um tipo de dado inteiro, um numero sem casas decimais

#float:
3.14 # isso é um numero com casas decimais, o python classifica ele como float, numeros com casas decimais.

#booleano:
True # isso é um valor booleano, o python classifica ele como booleano, um valor que pode ser verdadeiro ou falso.

# outros tipos de dados incluem:
lista = [1, 2, 3] # isso é uma lista, o python classifica ela como list uma colação de valores ordenados por indice e mutaveis.

tupla = ("a", "b", "c") # isso é uma tupla, o python classifica ele como tuple, uma coleção de valores ordenados e imutaveis.

dicionario = {"nome": "Antonio", "idade": 30} # isso é um dicionario, o python classifica ele como dict, uma coleção de pares chave-valor, onde cada chave é unica e tem um valor associado.

conjunto = {1, 2, 3} # isso é um conjunto, classificado como set é uma coleção de valores unicos que não possui índice, e a ordem dos elementos não deve ser considerada, ele não permite valores duplicados.

none = None # isso é um valor nulo, o python classifica ele como NoneType, represente a ausencia de valor ou um valor nulo, é usado para indicar que uma variavel não tem um valor definido ou que uma função não retorna nada.

# esses são os tipos de dados nativos de linguagem python, existem outros tipos de dados que podem ser criados usando classes e objetos, mas esses são os tipos de dados mais comuns e usados no dia a dia. 

# observação list, tuple, dict, e set também são chamados de tipos de dados compostos, pois eles podem armazenar multiplos valores, e são estruturas de dados mais complexas do que os tipos de dados simples como string, inteiro, float e booleano.

# resumindo: o python classifica os valores literais em tipos de dados, como string, inteiro, float, booleano, lista, tupla, dicionario, conjunto e NoneType. cada tipo de dado tem suas proprias caracteristicas e operações que podem ser realizadas com eles.